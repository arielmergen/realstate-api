#!/bin/bash

# Script de configuraci√≥n local para API RealState
echo "üöÄ Configurando API RealState para desarrollo local..."

# Detectar sistema operativo
detect_os() {
    case "$(uname -s)" in
        Darwin*)    OS="macos" ;;
        Linux*)     OS="linux" ;;
        CYGWIN*)    OS="windows" ;;
        MINGW*)     OS="windows" ;;
        MSYS*)      OS="windows" ;;
        *)          OS="unknown" ;;
    esac
    echo "üîç Sistema operativo detectado: $OS"
}

# Detectar sistema operativo al inicio
detect_os

# Funci√≥n para limpiar solo contenedores espec√≠ficos de RealState
clean_realstate_containers() {
    echo "üßπ Verificando contenedores anteriores de RealState..."
    
    # Verificar y eliminar contenedor de PostgreSQL si existe
    if docker ps -a --format "table {{.Names}}" | grep -q "realstate-postgres"; then
        echo "   - Eliminando contenedor realstate-postgres existente..."
        docker container rm -f realstate-postgres 2>/dev/null || true
    else
        echo "   - No hay contenedor realstate-postgres existente"
    fi
    
    # Verificar y eliminar contenedor de API si existe
    if docker ps -a --format "table {{.Names}}" | grep -q "realstate-api"; then
        echo "   - Eliminando contenedor realstate-api existente..."
        docker container rm -f realstate-api 2>/dev/null || true
    else
        echo "   - No hay contenedor realstate-api existente"
    fi
    
    # Detener servicios de docker-compose si est√°n ejecut√°ndose
    echo "   - Deteniendo servicios de docker-compose..."
    docker-compose down -v 2>/dev/null || true
    
    echo "‚úÖ Verificaci√≥n y limpieza completada"
}

# Funci√≥n para liberar puertos ocupados (multiplataforma)
free_occupied_ports() {
    echo "üîì Liberando puertos ocupados por instalaciones anteriores..."
    
    # Funci√≥n para liberar un puerto espec√≠fico
    free_port() {
        local port=$1
        local port_name=$2
        
        case "$OS" in
            "macos"|"linux")
                if lsof -Pi :$port -sTCP:LISTEN -t >/dev/null 2>&1; then
                    echo "   - Puerto $port ($port_name) est√° ocupado, liberando..."
                    
                    # Mostrar qu√© proceso est√° usando el puerto
                    local pid=$(lsof -Pi :$port -sTCP:LISTEN -t 2>/dev/null | head -1)
                    if [ ! -z "$pid" ]; then
                        local process_name=$(ps -p $pid -o comm= 2>/dev/null || echo "desconocido")
                        echo "     Proceso: $process_name (PID: $pid)"
                    fi
                    
                    # Intentar matar el proceso
                    lsof -Pi :$port -sTCP:LISTEN -t | xargs kill -9 2>/dev/null || true
                    sleep 3
                    
                    # Verificar si se liber√≥
                    if lsof -Pi :$port -sTCP:LISTEN -t >/dev/null 2>&1; then
                        echo "     ‚ö†Ô∏è  Puerto $port a√∫n ocupado, intentando con sudo..."
                        sudo lsof -Pi :$port -sTCP:LISTEN -t | xargs sudo kill -9 2>/dev/null || true
                        sleep 2
                    fi
                    
                    # Verificaci√≥n final
                    if lsof -Pi :$port -sTCP:LISTEN -t >/dev/null 2>&1; then
                        echo "     ‚ùå No se pudo liberar el puerto $port"
                        return 1
                    else
                        echo "     ‚úÖ Puerto $port liberado"
                        return 0
                    fi
                else
                    echo "   - Puerto $port ($port_name) est√° libre"
                    return 0
                fi
                ;;
            "windows")
                echo "   - En Windows, la liberaci√≥n de puertos se maneja autom√°ticamente"
                echo "   - Si hay conflictos, reinicia Docker Desktop"
                return 0
                ;;
            *)
                echo "   - Sistema operativo no soportado para liberaci√≥n de puertos"
                return 1
                ;;
        esac
    }
    
    # Liberar puertos
    free_port 3002 "API"
    free_port 5432 "PostgreSQL"
    free_port 3002 "API alternativa"
    
    echo "‚úÖ Proceso de liberaci√≥n de puertos completado"
}

