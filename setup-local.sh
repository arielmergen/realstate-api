# Funci√≥n para verificar si un puerto est√° disponible (multiplataforma)
check_port() {
    local port=$1
    
    case "$OS" in
        "macos"|"linux")
            # M√©todo 1: lsof (macOS/Linux)
            if command -v lsof >/dev/null 2>&1; then
                if lsof -Pi :$port -sTCP:LISTEN -t >/dev/null 2>&1; then
                    return 1  # Puerto ocupado
                fi
            fi
            
            # M√©todo 2: netstat (alternativo)
            if command -v netstat >/dev/null 2>&1; then
                if netstat -an 2>/dev/null | grep -q ":$port.*LISTEN"; then
                    return 1  # Puerto ocupado
                fi
            fi
            
            # M√©todo 3: nc (netcat) - intentar conectar
            if command -v nc >/dev/null 2>&1; then
                if nc -z localhost $port 2>/dev/null; then
                    return 1  # Puerto ocupado
                fi
            fi
            ;;
        "windows")
            # Para Windows (WSL/Git Bash)
            if command -v netstat >/dev/null 2>&1; then
                if netstat -an 2>/dev/null | grep -q ":$port.*LISTEN"; then
                    return 1  # Puerto ocupado
                fi
            fi
            
            # Usar PowerShell si est√° disponible
            if command -v powershell >/dev/null 2>&1; then
                if powershell -Command "Test-NetConnection -ComputerName localhost -Port $port -InformationLevel Quiet" 2>/dev/null | grep -q "True"; then
                    return 1  # Puerto ocupado
                fi
            fi
            ;;
        *)
            echo "‚ö†Ô∏è  Sistema operativo no soportado: $OS"
            return 1
            ;;
    esac
    
    return 0  # Puerto disponible
}

# Funci√≥n para encontrar un puerto disponible
find_available_port() {
    local start_port=$1
    local port=$start_port
    
    while ! check_port $port; do
        port=$((port + 1))
        if [ $port -gt $((start_port + 100)) ]; then
            echo "‚ùå No se encontr√≥ un puerto disponible en el rango $start_port-$((start_port + 100))"
            exit 1
        fi
    done
    echo $port
}

